#include "../includes/fdf.h"

void 	fdf_color(t_mlx *mlx, float z1, float z2)
{
	int 	z;

	if (z1 > z2)
		z = (int)z1;
	else
		z = (int)z2;
	mlx->color = 16777215 - (257 * z * 10);
}